class hello(Scene):
  def construct(self):
    obj_0_0_5 = Circle(radius=1, color="blue") 
    obj_0_0_5.set_x(0) 
    obj_0_0_5.set_y(0) 
    obj_0_0_5.scale(1) 
    obj_0_0_5.rotate(0.0, about_point=[0, 0, 0] ) 
    obj_0_1 = Circle(radius=1, color="red") 
    obj_0_1.set_x(2) 
    obj_0_1.set_y(1) 
    obj_0_1.scale(2) 
    obj_0_1.rotate(0.0, about_point=[2, 1, 0] ) 
    obj_1_0 = Square(color="blue") 
    obj_1_0.set_x(4) 
    obj_1_0.set_y(0) 
    obj_1_0.scale(1) 
